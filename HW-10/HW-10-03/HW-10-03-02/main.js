let number = prompt('Enter the number from 0 to 9.');
    
if (number == 0) {
    alert( '0=)' );
}else if (number == 1) {
    alert( '1=!' );
}else if (number == 2) {
    alert( '2=@' );
}else if (number == 3) {
    alert( '3=#' );
}else if (number == 4) {
    alert( '4=$' );
}else if (number == 5) {
    alert( '5=%' );
}else if (number == 6) {
    alert( '6=^' );
}else if (number == 7) {
    alert( '7=&' );
}else if (number == 8) {
    alert( '8=*' );
}else if (number == 9) {
    alert( '9=(' );
}else {
    alert( 'You made a mistake with the number. Try again.' );
}
